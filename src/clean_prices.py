def to_canonical(name: str):
    s = str(name).lower()
    for pattern, work, cat, lo, hi in RULES:
        if re.search(pattern, s):
            return work, cat, lo, hi
    return None, None, None, None


def main():
    base = os.path.dirname(__file__)
    raw = pd.read_csv(os.path.join(base, "..", "data", "real", "raw_prices.csv"))

    canon = raw["work_raw"].map(to_canonical)
    raw["canonical_work"] = [c[0] for c in canon]
    raw["category"] = [c[1] for c in canon]
    raw["lo"] = [c[2] for c in canon]
    raw["hi"] = [c[3] for c in canon]

    matched = raw.dropna(subset=["canonical_work"]).copy()
    n0 = len(matched)

    # Границы правдоподобия (доменные знания).
    plaus = matched[(matched["price"] >= matched["lo"]) &
                    (matched["price"] <= matched["hi"])].copy()
    n1 = len(plaus)

    # IQR-выбросы внутри каждой работы.
    def keep_inliers(g):
        q1, q3 = g["price"].quantile([0.25, 0.75])
        iqr = q3 - q1
        return g[(g["price"] >= q1 - 1.5 * iqr) & (g["price"] <= q3 + 1.5 * iqr)]

    clean = (plaus.groupby("canonical_work", group_keys=False)[plaus.columns]
             .apply(keep_inliers))

    print(f"Сырых строк: {len(raw)}")
    print(f"Классифицировано: {n0} ({n0/len(raw)*100:.0f}%)")
    print(f"Прошло проверку правдоподобия: {n1} (убрано {n0-n1} мис-извлечений)")
    print(f"После IQR-очистки: {len(clean)}")

    clean = clean[["canonical_work", "category", "unit_raw", "region", "price", "source"]]
    clean = clean.rename(columns={"unit_raw": "unit"})

    out_dir = os.path.abspath(os.path.join(base, "..", "data", "processed"))
    os.makedirs(out_dir, exist_ok=True)
    out = os.path.join(out_dir, "clean_prices.csv")
    clean.to_csv(out, index=False, encoding="utf-8-sig")

    print(f"\nКаноничных работ: {clean['canonical_work'].nunique()}")
    print("Строк по регионам:", clean.groupby("region").size().to_dict())
    print("Компаний-источников:", clean["source"].nunique())
    print("\nМедианная цена по регионам:")
    print(clean.groupby("region")["price"].median().round(0).to_string())
    print("\nМедианы по работам (топ-15):")
    piv = (clean.pivot_table(index="canonical_work", columns="region",
                             values="price", aggfunc="median").round(0))
    print(piv.head(15).to_string())
    print(f"\nФайл: {out}")


if __name__ == "__main__":
    main()
