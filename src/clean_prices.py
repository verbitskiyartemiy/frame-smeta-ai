"""
Очистка и нормализация собранных реальных цен.

Вход:  data/real/raw_prices.csv     (сырые строки из прайс-листов)
Выход: data/processed/clean_prices.csv  (готово для обучения модели)

Что делаем:
1. Каждое "сырое" название работы приводим к КАНОНИЧЕСКОЙ работе по ключевым словам
   (например, "Выравнивание потолка ротбандом по маякам" -> "Штукатурка потолка").
2. Строки, которые не удалось уверенно классифицировать, отбрасываем.
3. Убираем выбросы по цене внутри каждой канонической работы (робастно, по IQR).
4. Сохраняем чистый датасет: canonical_work, category, unit, region, price.
"""
from __future__ import annotations
import os
import re
import pandas as pd

# Правила: (ключевые слова-регекс, каноническая работа, категория).
# Порядок важен — сначала демонтаж, потом отделка. Первое совпадение выигрывает.
RULES = [
    # ---- Демонтаж ----
    (r"(демонтаж|удален|очистк|размывк|зачистк|снят|разбор|разбивк).*обо", "Демонтаж обоев", "Демонтаж"),
    (r"(демонтаж|удален|снят|разбор).*плитк",                              "Демонтаж плитки", "Демонтаж"),
    (r"(демонтаж|удален|снят|разбор).*(стяжк|пол|ламинат|линолеум|паркет)","Демонтаж пола", "Демонтаж"),
    (r"(демонтаж|разбор|снят).*(перегород|стен|кладк)",                    "Демонтаж перегородки", "Демонтаж"),
    (r"(демонтаж|разбор|снят|удален).*(потол|armstrong|армстронг|реечн|гкл)","Демонтаж потолка", "Демонтаж"),
    (r"(демонтаж|удален|снят|разбор|очистк|размывк|зачистк|обеспыл)",       "Демонтаж прочее", "Демонтаж"),
    # ---- Стены ----
    (r"штукатурк.*стен|выравниван.*стен|ротбанд.*стен",                    "Штукатурка стен", "Стены"),
    (r"шпакл.*стен|шпатл.*стен",                                           "Шпаклёвка стен", "Стены"),
    (r"грунтов",                                                           "Грунтовка", "Стены"),
    (r"оклейк.*обо|поклейк.*обо|обои|обоев",                               "Поклейка обоев", "Стены"),
    (r"(окрас|покрас).*стен",                                              "Покраска стен", "Стены"),
    (r"(укладк|облицовк).*плитк.*стен|плитк.*стен",                        "Плитка на стену", "Стены"),
    # ---- Пол ----
    (r"стяжк",                                                             "Стяжка пола", "Пол"),
    (r"наливной пол|наливн",                                               "Наливной пол", "Пол"),
    (r"ламинат",                                                           "Укладка ламината", "Пол"),
    (r"(укладк|облицовк).*плитк.*пол|плитк.*пол|напольн.*плитк",           "Плитка на пол", "Пол"),
    (r"плинтус",                                                           "Плинтус", "Пол"),
    (r"(укладк|облицовк).*плитк|плитк|керамогранит",                       "Укладка плитки", "Пол"),
    # ---- Потолок ----
    (r"(штукатурк|шпакл|выравниван|ротбанд).*потол|потол.*(шпакл|штукатурк)","Штукатурка потолка", "Потолок"),
    (r"натяжн.*потол|натяжн",                                              "Натяжной потолок", "Потолок"),
    (r"(гкл|гипсокартон).*потол|потол.*(гкл|гипсокартон)",                 "Потолок из ГКЛ", "Потолок"),
    (r"(окрас|покрас).*потол",                                             "Покраска потолка", "Потолок"),
    # ---- Гипсокартон / перегородки ----
    (r"(перегород|стен).*(гкл|гипсокартон)|(гкл|гипсокартон).*(перегород|стен)","Перегородка из ГКЛ", "Стены"),
    # ---- Электрика ----
    (r"штроб",                                                             "Штробление", "Электрика"),
    (r"кабел|провод",                                                      "Прокладка кабеля", "Электрика"),
    (r"розетк|выключат|подрозетник",                                       "Монтаж розеток", "Электрика"),
    (r"светильник|люстр|бра|споты",                                        "Монтаж светильников", "Электрика"),
    (r"электрощит|распредщит|автомат|щиток|щит ",                          "Электрощит", "Электрика"),
    # ---- Сантехника ----
    (r"труб|канализац|водоснаб|водопровод|разводк.*(вод|сантех)",          "Разводка сантехники", "Сантехника"),
    (r"смесител",                                                          "Установка смесителя", "Сантехника"),
    (r"унитаз|инсталляц",                                                  "Установка унитаза", "Сантехника"),
    (r"ванн",                                                              "Установка ванны", "Сантехника"),
    (r"раковин|умывальник|мойк",                                           "Установка раковины", "Сантехника"),
    # ---- Двери ----
    (r"межкомнатн.*двер|двер.*межкомнат",                                  "Межкомнатная дверь", "Двери"),
    (r"входн.*двер",                                                       "Входная дверь", "Двери"),
    (r"откос",                                                             "Откосы", "Двери"),
]


def to_canonical(name: str):
    s = str(name).lower()
    for pattern, work, cat in RULES:
        if re.search(pattern, s):
            return work, cat
    return None, None


def main():
    base = os.path.dirname(__file__)
    raw = pd.read_csv(os.path.join(base, "..", "data", "real", "raw_prices.csv"))

    canon = raw["work_raw"].map(to_canonical)
    raw["canonical_work"] = [c[0] for c in canon]
    raw["category"] = [c[1] for c in canon]

    matched = raw.dropna(subset=["canonical_work"]).copy()
    print(f"Сырых строк: {len(raw)} | классифицировано: {len(matched)} "
          f"({len(matched)/len(raw)*100:.0f}%)")

    # Убираем выбросы по цене внутри каждой (работа) — робастно по IQR.
    def drop_outliers(g):
        q1, q3 = g["price"].quantile([0.25, 0.75])
        iqr = q3 - q1
        lo, hi = q1 - 1.5 * iqr, q3 + 1.5 * iqr
        return g[(g["price"] >= lo) & (g["price"] <= hi)]

    clean = (matched.groupby("canonical_work", group_keys=False)
             .apply(drop_outliers))
    print(f"После удаления выбросов: {len(clean)}")

    clean = clean[["canonical_work", "category", "unit_raw", "region", "price", "source"]]
    clean = clean.rename(columns={"unit_raw": "unit"})

    out_dir = os.path.abspath(os.path.join(base, "..", "data", "processed"))
    os.makedirs(out_dir, exist_ok=True)
    out = os.path.join(out_dir, "clean_prices.csv")
    clean.to_csv(out, index=False, encoding="utf-8-sig")

    print(f"\nКаноничных работ: {clean['canonical_work'].nunique()}")
    print("По регионам:", clean.groupby("region").size().to_dict())
    print("\nМедианная цена по регионам (проверка: Москва > СПб > Регион):")
    print(clean.groupby("region")["price"].median().round(0).to_string())
    print("\nПримеры (медиана цены по работе x регион):")
    piv = (clean.pivot_table(index="canonical_work", columns="region",
                             values="price", aggfunc="median")
           .round(0))
    print(piv.head(15).to_string())
    print(f"\nФайл: {out}")


if __name__ == "__main__":
    main()
